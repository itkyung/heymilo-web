package com.heymilo.ui.param;

public class RegistParam {
	private String registLoginId;
	private String registPassword;
	private String registName;
	
	private String facebookToken;

	public String getRegistLoginId() {
		return registLoginId;
	}

	public void setRegistLoginId(String registLoginId) {
		this.registLoginId = registLoginId;
	}

	public String getRegistPassword() {
		return registPassword;
	}

	public void setRegistPassword(String registPassword) {
		this.registPassword = registPassword;
	}

	public String getRegistName() {
		return registName;
	}

	public void setRegistName(String registName) {
		this.registName = registName;
	}

	public String getFacebookToken() {
		return facebookToken;
	}

	public void setFacebookToken(String facebookToken) {
		this.facebookToken = facebookToken;
	}
	
	
}
